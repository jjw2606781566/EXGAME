  CREATE OR REPLACE EDITIONABLE FUNCTION  "FUN_GET_GAME_PRICE" (gid in varchar2) return number 
as 
  rate number;
  final_price number;   --the final price of the game after discount
  ori_price number;
  rate_num number;
begin  
  rate := 1;
  
  select price into ori_price
  from game
  where game.id = gid;
  
  select count(discount_rate) into rate_num
  from discount 
  where game_id=gid
    and start_time <= (select sysdate from dual) 
    and end_time >= (select sysdate from dual);

  if (rate_num > 0) then
    select discount_rate into rate
    from discount
    where game_id = gid
      and start_time <= (select sysdate from dual) 
      and end_time >= (select sysdate from dual);
  end if;
  
  final_price := rate * ori_price; 
  
  return final_price;
END FUN_GET_GAME_PRICE;

